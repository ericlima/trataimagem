package trataImagens;

public class ResultadoProvaDTO {

	private String codigoProva;
	private String ausente;
	private String respostas;
	
	public String getCodigoProva() {
		return codigoProva;
	}
	public void setCodigoProva(String codigoProva) {
		this.codigoProva = codigoProva;
	}
	public String getAusente() {
		return ausente;
	}
	public void setAusente(String ausente) {
		this.ausente = ausente;
	}
	public String getRespostas() {
		return respostas;
	}
	public void setRespostas(String respostas) {
		this.respostas = respostas;
	}
	
}
